package au.com.BI.MultiMedia.AutonomicHome.Commands;

public class AutonomicHomeCommandException extends Exception {

	public AutonomicHomeCommandException() {
		super();
	}

	public AutonomicHomeCommandException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AutonomicHomeCommandException(String arg0) {
		super(arg0);
	}

	public AutonomicHomeCommandException(Throwable arg0) {
		super(arg0);
	}

}
